void	ft_rev_int_tab(int *tab, int size)
{
	int	a;
	int	i;

	i = 0;
	while (i < size / 2)
	{
	a = tab[size - i - 1];
	tab[size - i - 1] = tab[i];
	tab[i] = a;
	i++;
	}
}
